//    --------------------------------------Save Button------------------------------
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String id=jLabel3.getText();
        String name=jTextField1.getText();
        String mobilenumber=jTextField2.getText();
        String email=jTextField3.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        
        String bloodgroup=(String)jComboBox2.getSelectedItem();
        String age=jTextField4.getText();
        String gymtime=(String)jComboBox3.getSelectedItem();
        String amount=jTextField5.getText();
        
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into member values (?,?,?,?,?,?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, mobilenumber);
            ps.setString(4, email);
            ps.setString(5, gender);
            ps.setString(6, bloodgroup);
            ps.setString(7, age);
            ps.setString(8,gymtime);
            ps.setString(9,amount);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully Saved Member Details");
            setVisible(false);
            new NewMember().setVisible(true);   
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

